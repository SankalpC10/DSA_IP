package a_Introduction;

// Java Hello World Program -- single Line Comment
public class a_hello_world {
    public static void main(String[] args) { /* public is access specifier -- main function get accessible from everywhere
                                              * static is a special method which can work even without creating an object.
                                              * void is the return type
                                              * String[] args allows us to pass arguments on command line
                                              */
        /*
            This is an example
            of multiline Comment
            CLI Commands
            $ javac a_hello_world.java - to build
            $ java a_hello_world - to run
        */
        System.out.println("Hello World");
    }
}
