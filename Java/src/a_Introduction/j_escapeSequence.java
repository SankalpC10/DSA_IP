public class j_escapeSequence {
    public static void main(String[] args) {
        System.out.println("Welcome to \"Area 51\"!");
        System.out.println("Welcome to 'Miami Vice'!");
        System.out.println("Hi \nWelcome Here");
        // valid \ sequences are \t-tab,\b,\n,\r,\f,\',\",\\
        System.out.println("C:\\xyz\\abc.txt");
    }
}
