package a_Introduction;

/*
* Variable naming-
* invalid 1a,a#,else,do
* valid a1,a$,$a
* camel case naming convention for variables: playerName
* Const: MAX_AGE = 20
* */

public class b_variables_and_data_types {
    public static void main(String[] args) {
        int age = 22; //primitive
        String name = "Sankalp";
        double v = 70.5;
        System.out.println(age);
        System.out.println(name);
        System.out.println(v);
        // below datatypes are primitive
        boolean isvalid = true;
        byte marks = 90;
        float pi = (float) 3.14;
        float div = 15.0F/4.0F;
        long views = 100000000;
        char gender = 'M';
    }
}

